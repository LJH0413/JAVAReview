package java01_datatype;

public class JungsuType {
	public static void main(String[] args) {
		
		// 1번 
		int korea;
		int math;
		int english;
		int total;
		
		// 2번 
		korea = 89;
		math = 78; 
		english = 99;
		
		System.out.println("국어: " + korea);
		System.out.println("수학: " + math);
		System.out.println("영어: " + english);
		
		// 3번 
		int totalScore = (korea + english + math);		
		total = totalScore;
		
		// 4번 
		System.out.println("국영수 총점: " + total + "점");
	}
	
	

}
